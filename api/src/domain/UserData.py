class UserData:
    def __init__(self,
        key = None,
        name = None,
        firstName = None,
        lastName = None,
        email = None,
        pictureUrl = None,
        roles = None
    ):
        self.key = key
        self.name = name
        self.firstName = firstName
        self.lastName = lastName
        self.email = email
        self.pictureUrl = pictureUrl
        self.roles = roles
