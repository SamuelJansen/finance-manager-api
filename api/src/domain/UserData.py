class UserData:
    def __init__(self,
        key = None,
        name = None,
        firstName = None,
        lastName = None,
        email = None,
        picture = None,
        roleList = None,
        status = None
    ):
        self.key = key
        self.name = name
        self.firstName = firstName
        self.lastName = lastName
        self.email = email
        self.picture = picture
        self.roleList = roleList
        self.status = status
